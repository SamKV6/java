import java.util.ArrayList;
import java.util.List;

class TextPrinter {
    private final String text;
    
    // Конструктор принимает большой текст
    public TextPrinter(String text) {
        this.text = text;
    }
    
    //Синхронизированный метод для вывода текста
    public static synchronized void printText(String text) {
        System.out.println("Поток ID: " + Thread.currentThread().getId());
        System.out.println(text);
        System.out.println("-".repeat(50));
    }
}

class PrintThread extends Thread {
    private final String text;
    
    public PrintThread(String text) {
        this.text = text;
    }
    
    @Override
    public void run() {
        TextPrinter.printText(text);
    }
}

